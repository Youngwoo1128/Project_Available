package com.mac_available.carrotmarket;

public class Item {

    public int img;
    public String name;
    public String location;
    public String time;
    public String price;


    public Item(){

    }

    public Item(int img, String name, String location, String time, String price){
        this.img = img;
        this.name = name;
        this.location = location;
        this.time = time;
        this.price = price;
    }


}
